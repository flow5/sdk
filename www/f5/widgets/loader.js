/*global F5*/

(function () {	
	function Loader() {		
		this.construct = function (data) {			
			var template = F5.loadTemplate(this.el.getAttribute('f5template'), data);
			this.el.appendChild(template);
		};
	}
	
	F5.Prototypes.Widgets.Loader = new Loader();	
}());